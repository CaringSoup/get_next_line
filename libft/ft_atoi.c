#include "libft.h"

int		ft_atoi(const char *str)
{
	long	i;
	int		sign;
	long	res;

	i = 0;
	res = 0;
	sign = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\r'
			|| str[i] == '\f' || str[i] == '\v')
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
	{
		if (res < 0)
			return (0 - (sign == 1));
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	return (sign * res);
}
